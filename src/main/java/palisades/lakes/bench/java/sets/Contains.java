  public static final int 
  countContains (final IntegerInterval[] s0,
                 final int[] s1) {
    int k = 0;
    final int n = s0.length;
    assert n == s1.length;
    for (int i=0;i<n;i++) { 
      if (contains(s0[i],s1[i])) { k++; } }
    return k; }

