  public static final int 
  countStatic (final IntegerInterval[] s,
               final int[] x) {
    int k = 0;
    final int n = s.length;
    assert n == x.length;
    for (int i=0;i<n;i++) { 
      if (contains(s[i],x[i])) { k++; } }
    return k; }

